def clean_address(text):
    """
    清洗地址字符串，去除干扰项
    """
    if not text: return ""
    # 去除两端的特殊符号
    text = re.sub(r'^[\s,，。\.；;]+|[\s,，。\.；;]+$', '', text)
    return text.strip()

def get_province(text, ai_suggested=""):
    """
    从地址或 AI 建议中确定省份
    """
    # 1. 优先从 AI 提取的省份里找
    if ai_suggested:
        for p in PROVINCES:
            if p in ai_suggested:
                return p
    
    # 2. 如果没有 AI 建议或不匹配，直接从地址正文里找
    if text:
        for p in PROVINCES:
            if p in text:
                return p
        for city, province in CITY_PROVINCE_HINTS.items():
            if city in text:
                return province
                
    return None
